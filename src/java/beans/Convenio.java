package beans;
public class Convenio {
    public int id_convenio;
    public String nome_da_empresa;
    public String cnpj;
    public String area_de_atuacao;
    public String data_inicio_convenio;
    public String duracao_convenio;
    

    public int getId_convenio() {
        return id_convenio;
    }

    public void setId_convenio(int id_convenio) {
        this.id_convenio = id_convenio;
    }

    public String getNome_da_empresa() {
        return nome_da_empresa;
    }

    public void setNome_da_empresa(String nome_da_empresa) {
        this.nome_da_empresa = nome_da_empresa;
    }

    public String getCnpj() {
        return cnpj;
    }

    public void setCnpj(String cnpj) {
        this.cnpj = cnpj;
    }

    public String getArea_de_atuacao() {
        return area_de_atuacao;
    }

    public void setArea_de_atuacao(String area_de_atuacao) {
        this.area_de_atuacao = area_de_atuacao;
    }

    public String getData_inicio_convenio() {
        return data_inicio_convenio;
    }

    public void setData_inicio_convenio(String data_inicio_convenio) {
        this.data_inicio_convenio = data_inicio_convenio;
    }

    public String getDuracao_convenio() {
        return duracao_convenio;
    }

    public void setDuracao_convenio(String duracao_convenio) {
        this.duracao_convenio = duracao_convenio;
    }
    
    
}

